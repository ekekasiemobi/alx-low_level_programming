int add(int a, int b) {
    return a + b;
}

int sub(int a, int b) {
    return a - b;
}

int mul(int a, int b) {
    return a * b;
}

/**
 * mod - checks if an integer a is divisible by an integer b
 * @a: integer
 * @b: integer
 * Return: a % b
 */
int div(int a, int b) {
    return a / b;
}

int mod(int a, int b) {
    return a % b;
}

